public class Student {
    private String enroll;
    private String name;
    private int roll;
    private String div;

    public Student() {
        this.enroll = "un";
    }

    public Student(String enroll, String name, int roll, String div) {
        this.enroll = enroll;
        this.name = name;
        this.roll = roll;
        this.div = div;
    }
}